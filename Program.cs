using FirebaseAdmin;
using Google.Apis.Auth.OAuth2;
using Google.Cloud.Firestore;
using Anthropic.SDK;
using HealthSpark.Services;
using FirebaseAdmin.Auth;

var builder = WebApplication.CreateBuilder(args);

// ?? Firebase Setup ?????????????????????????????????????????

var firebaseApp = FirebaseApp.Create(new AppOptions
{
    Credential = GoogleCredential.FromFile(
        builder.Configuration["Firebase:CredentialPath"]
    )
});

var firestoreDb = await FirestoreDb.CreateAsync(
    builder.Configuration["Firebase:ProjectId"]
);

// ?? Register Services ??????????????????????????????????????

builder.Services.AddSingleton(firebaseApp);
builder.Services.AddSingleton(firestoreDb);
builder.Services.AddSingleton(FirebaseAuth.GetAuth(firebaseApp));

var anthropicKey = builder.Configuration["Anthropic:ApiKey"] ?? string.Empty;
builder.Services.AddSingleton(new AnthropicClient(anthropicKey));
Console.WriteLine($"Anthropic Key loaded: {anthropicKey[..10]}...");

builder.Services.AddScoped<FirebaseService>();
builder.Services.AddScoped<AuthService>();
builder.Services.AddScoped<AiAssessmentService>();

// ?? MVC Setup ??????????????????????????????????????????????

builder.Services.AddControllersWithViews();

builder.Services.AddSession(options =>
{
    options.IdleTimeout = TimeSpan.FromHours(8);
    options.Cookie.HttpOnly = true;
    options.Cookie.IsEssential = true;
});

var app = builder.Build();

// ?? Middleware Pipeline ????????????????????????????????????

if (!app.Environment.IsDevelopment())
{
    app.UseExceptionHandler("/Account/Login");
    app.UseHsts();
}

app.UseHttpsRedirection();
app.UseStaticFiles();
app.UseRouting();
app.UseSession();
app.UseAuthorization();

// ?? Routes ?????????????????????????????????????????????????

app.MapControllerRoute(
    name: "default",
    pattern: "{controller=Account}/{action=Login}/{id?}"
);

app.Run();